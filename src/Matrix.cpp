#include "Matrix.h"

MatrixOrder flipOrder(MatrixOrder order)
{
    if (order == MatrixOrder::RowMajor)
    {
        return MatrixOrder::ColumnMajor;
    }
    return MatrixOrder::RowMajor;
}

